/**
 * generate-pptx.ts
 *
 * Generates a styled .pptx from slides.md.
 *
 * Architecture: companion source/render pairs (per CLAUDE.md whiteboard
 * stylizer design). For every slide whose first speaker-note line is
 * `whiteboard=1`, the generator emits two physical slides:
 *
 *   - SOURCE slide  (filmstrip pos N, slide.hidden = true)
 *       Native, individually-editable primitives:
 *         - real addText for titles/body
 *         - real addTable for tables
 *         - real addChart (BAR / LINE / PIE) for chart showcases
 *         - real addShape compositions (rect / roundRect / line / ellipse +
 *           arrow endings) for flow / pillars / hub-spoke / roadmap
 *         - real addShape('line') for title underlines and section dividers
 *       Hidden in presentation mode but still visible in the filmstrip,
 *       so a human (or future restyle skill) can edit the source of truth.
 *
 *   - RENDER slide  (filmstrip pos N+1, visible)
 *       Same text, but tables / charts / diagrams / underlines are placed
 *       as rough.js PNG overlays at the same bounds. This is what the
 *       audience sees during a presentation.
 *
 * Both members of a pair carry a stable `pairId` in their speaker notes,
 * appended after any human notes via the sentinel
 *
 *     |This is autogenerated, please don't touch={...json...}
 *
 * so future tooling (e.g. a `/restylize` skill) can find the partner of
 * any slide and re-render the rough.js PNG overlays from the source's
 * current state.
 *
 * Slides without `whiteboard=1` get a single plain slide on the default
 * theme — no whiteboard background, no rough overlays.
 *
 * Usage: npx tsx generate-pptx.ts <slides.md> <output.pptx>
 */

