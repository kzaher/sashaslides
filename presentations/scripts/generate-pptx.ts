// ── Slide parsing ──────────────────────────────────────────────────────────

type SlideData = {
  title: string;
  subtitle: string;
  bodyLines: string[];
  table: { headers: string[]; rows: string[][] } | null;
  notes: string;
  isSectionHeader: boolean;
  isTitleSlide: boolean;
  isClosing: boolean;
  whiteboard: boolean; // first `>` line was `whiteboard=1`
};

function parseSlides(md: string): SlideData[] {
  const blocks = md.split(/\n---\n/);
  const slides: SlideData[] = [];

  for (const block of blocks) {
    const lines = block.trim().split("\n").filter((l) => l.trim() !== "");
    if (lines.length === 0) continue;

    let title = "";
    let subtitle = "";
    const bodyLines: string[] = [];
    const notes: string[] = [];
    let isSectionHeader = false;
    let table: SlideData["table"] = null;
    let inTable = false;

    for (let li = 0; li < lines.length; li++) {
      const line = lines[li];

      if (line.startsWith(">")) {
        notes.push(line.replace(/^>\s*/, ""));
        continue;
      }

      if (line.includes("|") && !line.startsWith("#")) {
        const cells = line.split("|").map((c) => c.trim()).filter(Boolean);
        if (/^[\s|:-]+$/.test(line)) continue;
        if (!inTable) {
          const next = lines[li + 1];
          if (next && /^[\s|:-]+$/.test(next)) {
            table = { headers: cells, rows: [] };
            inTable = true;
            continue;
          }
        }
        if (inTable && table) {
          table = { ...table, rows: [...table.rows, cells] };
          continue;
        }
      } else {
        inTable = false;
      }

      if (/^#{1,2}\s/.test(line)) {
        const text = line.replace(/^#+\s+/, "").replace(/\*\*/g, "");
        if (text.startsWith("PART ")) {
          isSectionHeader = true;
          continue;
        }
        if (!title) title = text;
        else if (!subtitle) subtitle = text;
        continue;
      }

      bodyLines.push(line);
    }

    // Opt-in marker MUST be the first `>` line on the slide. Strip it
    // before computing subtitle/notes so it never leaks into displayed text.
    let whiteboard = false;
    if (notes[0] === "whiteboard=1") {
      whiteboard = true;
      notes.shift();
    }

    const isTitleSlide =
      lines[0]?.startsWith("# ") && bodyLines.length === 0 && !isSectionHeader && !table;
    const isClosing = title.toLowerCase().includes("thank you");

    if (notes.length > 0 && !subtitle) subtitle = notes[0];
    if (isTitleSlide && notes.length > 0) subtitle = notes.join(" | ");

    slides.push({
      title,
      subtitle,
      bodyLines,
      table,
      notes: notes.join("\n"),
      isSectionHeader,
      isTitleSlide,
      isClosing,
      whiteboard,
    });
  }

  return slides;
}

function cleanText(s: string): string {
  return s.replace(/\*\*/g, "");
}

function formatBodyText(line: string): { text: string; bold: boolean; bullet: boolean } {
  let text = line;
  let bullet = false;
  let bold = false;

  if (text.startsWith("- ") || text.startsWith("• ")) {
    text = text.slice(2);
    bullet = true;
  }
  const numMatch = text.match(/^(\d+)\.\s+(.*)/);
  if (numMatch) {
    text = numMatch[2];
    bullet = true;
  }
  if (line.match(/^\*\*[^*]+\*\*$/) || line.match(/^\*\*[^*]+:\*\*$/)) {
    bold = true;
  }
  text = cleanText(text);
  return { text, bold, bullet };
}

// ── Pair-id + sentinel notes ──────────────────────────────────────────────

const SENTINEL_PREFIX = "|This is autogenerated, please don't touch=";

/** Deterministic id derived from a seed string so re-running the generator
 *  yields the same pairId for the same logical slide. */
function pairIdFor(seed: string): string {
  let h = 0;
  for (let i = 0; i < seed.length; i++) {
    h = (h * 31 + seed.charCodeAt(i)) >>> 0;
  }
  return `pair_${h.toString(16).padStart(8, "0")}`;
}

function buildNotes(realNotes: string, payload: object): string {
  const json = JSON.stringify(payload);
  const sep = realNotes && !realNotes.endsWith("\n") ? "\n" : "";
  return `${realNotes}${sep}${SENTINEL_PREFIX}${json}`;
}

